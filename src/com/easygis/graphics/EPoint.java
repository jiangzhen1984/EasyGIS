package com.easygis.graphics;

public class EPoint {
	
	public double x;
	public double y;
	public double z;

	public EPoint() {
	}

	public EPoint(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public EPoint(double x, double y, double z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	
	
	

}
